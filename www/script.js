// Функция для отображения категорий и очков
function displayTable() {
    const table = document.getElementById("game-table");
    
    categories.forEach(category => {
        const row = document.createElement("tr");
        
        // Столбец категории
        const categoryCell = document.createElement("td");
        categoryCell.innerText = category.name;
        row.appendChild(categoryCell);

        // Столбцы с очками
        category.questions.forEach(question => {
            const questionCell = document.createElement("td");
            questionCell.innerText = question.points;
            questionCell.onclick = () => showContent(question, questionCell);
            row.appendChild(questionCell);
        });
        
        table.appendChild(row);
    });
}

// Функция для отображения содержимого ячейки
function showContent(question, cell) {
    cell.innerText = "";  // Очистка ячейки после выбора
    cell.classList.add("selected"); // Добавление класса для изменения цвета
    contentDisplay.innerHTML = "";  // Очистка модального окна

    if (question.type === "text") {
        contentDisplay.innerText = question.content;
    } else if (question.type === "image") {
        const img = document.createElement("img");
        img.src = question.content;
        img.alt = "Вопрос изображение";
        img.style.maxWidth = "100%";
        contentDisplay.appendChild(img);

        if (question.description) {
            const descriptionText = document.createElement("p");
            descriptionText.innerText = question.description;
            descriptionText.style.marginTop = "10px";  // Отступ сверху
            contentDisplay.appendChild(descriptionText);
        }
    } else if (question.type === "audio") {
        const audio = document.createElement("audio");
        audio.src = question.content;
        audio.controls = true;
        contentDisplay.appendChild(audio);

        if (question.description) {
            const descriptionText = document.createElement("p");
            descriptionText.innerText = question.description;
            descriptionText.style.marginTop = "10px";  // Отступ сверху
            contentDisplay.appendChild(descriptionText);
        }
    } else if (question.type === "video") {
        const video = document.createElement("video");
        video.src = question.content;
        video.controls = true;
        video.style.maxWidth = "100%";
        contentDisplay.appendChild(video);

        if (question.description) {
            const descriptionText = document.createElement("p");
            descriptionText.innerText = question.description;
            descriptionText.style.marginTop = "10px";  // Отступ сверху
            contentDisplay.appendChild(descriptionText);
        }
    }

    // Кнопка для показа правильного ответа
    const answerButton = document.createElement("button");
    answerButton.innerText = "Показать правильный ответ";
    answerButton.style.marginTop = "20px";
    answerButton.onclick = () => {
        const answerText = document.createElement("p");
        answerText.innerText = `${question.correctAnswer}`;
        answerText.style.fontWeight = "bold";
        answerText.style.color = "#007bff"; // Цвет для текста ответа
        contentDisplay.appendChild(answerText);
        answerButton.disabled = true; // Деактивируем кнопку после нажатия

        if (question.answerContent) {
            const answerContent = document.createElement(question.answerType === "image" ? "img" : "video");
            answerContent.src = question.answerContent;
            answerContent.alt = "Правильный ответ";
            answerContent.style.maxWidth = "100%";

            if (question.answerType === "video") {
                answerContent.controls = true; // Добавление управления для видео
            }

            contentDisplay.appendChild(answerContent);
        }

        answerButton.disabled = true; // Деактивируем кнопку после нажатия
    };

    contentDisplay.appendChild(answerButton);
    document.getElementById("content-modal").classList.remove("hidden");
}

// Функция для закрытия модального окна
function closeModal() {
    document.getElementById("content-modal").classList.add("hidden");
}

document.getElementById("close-modal").onclick = closeModal;

